package homeWorks;

public class IfBlocks {
    public static void main(String[] args) {

        int sayi = 19;
        if(sayi < 20){
            System.out.println("sayi 20'den küçüktür");
        }else if(sayi == 20){
            System.out.println("sayi 20'dir");
        }
        else{
            System.out.println("sayi 20'den küçük değildir");
        }

        if (sayi < 15){
            System.out.println("sayi 15'ten küçüktür");
        }

    }
}
